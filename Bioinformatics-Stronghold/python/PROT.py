def translating_protein(codons: dict = RNA_CODONS, rna: str = RNA):
    protein = ""
    for i in range(0, len(rna), 3):
        codon = rna[i : i + 3]
        amino_acids = codons.get(codon)
        if amino_acids is not None and amino_acids != "Stop":
            protein += str(amino_acids)
    return protein


